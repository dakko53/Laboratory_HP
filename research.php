          私達の研究室では、これらの植物が有する脂質に注目し、植物が外界の環境に適応するためにどのように脂質を変化させるのか、
          また、進化の過程で、脂質はどのように変化していったのかを解明することを目指しています。また当研究室ではそれらの知見を活用し、
          植物や藻類で有用脂質を高生産する仕組みを作ることも目指しています。
